    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)SubjectTable.getModel();
        int SelectedRows = SubjectTable.getSelectedRow();
        
        try {
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            con = DB.DBConnect.getConnection();
            
            pst = con.prepareStatement("update subjects set subjectName = ?, subjectCode = ?, year = ?, semester = ?, lecHours = ?, tuteHours = ?, labHours = ?, eveHours = ? where idSubjects = ?");
            
                     
        pst.setString(1, Subject_Name.getText());
        pst.setString(2, Subject_Code.getText());
        pst.setString(3, (String) year.getSelectedItem());
        pst.setString(4, (String) semester.getSelectedItem());
        pst.setString(5, (String) Lecture_Hours.getSelectedItem());
        pst.setString(6, (String) Tutorial_Hours.getSelectedItem());
        pst.setString(7, (String) Practical_Hours.getSelectedItem());
        pst.setString(8, (String) Evaluation_Hours.getSelectedItem());
        pst.setInt(9, id);
        
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Updated Successfully");
        upDateDB();
        
        Subject_Name.setText("");
        Subject_Code.setText("");
        year.setSelectedIndex(0);
        semester.setSelectedIndex(0);
        Lecture_Hours.setSelectedIndex(0);
        Tutorial_Hours.setSelectedIndex(0);
        Practical_Hours.setSelectedIndex(0);
        Evaluation_Hours.setSelectedIndex(0);
        
        } catch (SQLException e) {
         Logger.getLogger(Manage_Subjects.class.getName()).log(Level.SEVERE, null, e);
        }                 
    }//GEN-LAST:event_UpdateButtonActionPerformed

