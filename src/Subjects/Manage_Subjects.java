    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)SubjectTable.getModel();
        int SelectedRows = SubjectTable.getSelectedRow();
        
        try {
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            
            deleteItem = JOptionPane.showConfirmDialog(null, "Do you want to delete?", "Warning",JOptionPane.YES_NO_OPTION);
            
            if (deleteItem == JOptionPane.YES_OPTION) {
                con = DB.DBConnect.getConnection();
                pst = con.prepareStatement("delete from subjects where idSubjects = ?");
                
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Deleted Successfully");
                upDateDB();
                
                Subject_Name.setText("");
                Subject_Code.setText("");
                year.setSelectedIndex(0);
                semester.setSelectedIndex(0);
                Lecture_Hours.setSelectedIndex(0);
                Tutorial_Hours.setSelectedIndex(0);
                Practical_Hours.setSelectedIndex(0);
                Evaluation_Hours.setSelectedIndex(0);
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

