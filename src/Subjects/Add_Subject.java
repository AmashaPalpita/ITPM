    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        if (Subject_Name.getText().trim().isEmpty() && Subject_Code.getText().trim().isEmpty() && year.getSelectedItem() == "SELECT") {
            Error_SN.setText("Subject Name is empty!");
            Error_SC.setText("Subject Code is empty!");
            Error_OY.setText("Offered Year is not selected!");
            Error_OS.setText("Offered Semester is not selected!");
        } else if (Subject_Name.getText().trim().isEmpty()) {
            Error_SN.setText("Subject Name is empty!");
        } else if (Subject_Code.getText().trim().isEmpty()) {
            Error_SC.setText("Subject Code is empty!");
        } else if (year.getSelectedItem() == "SELECT") {
            Error_OY.setText("Offered Year is not selected!");
        } //else if (!Sem1.isSelected() || !Sem2.isSelected()) {
           // Error_OS.setText("Offered Semester is not selected!");
        //} 
        else {
        try{
         con = DB.DBConnect.getConnection(); 
         pst = con.prepareStatement("insert into subjects(subjectName,subjectCode,year,semester,lecHours,tuteHours,labHours,eveHours)value(?,?,?,?,?,?,?,?)");
         
        pst.setString(1, Subject_Name.getText());
        pst.setString(2, Subject_Code.getText());
        pst.setString(3, (String) year.getSelectedItem());
        
        String sem = null;
        if(Sem1.isSelected()) {
            sem = Sem1.getText();
        } if (Sem2.isSelected()) {
            sem = Sem2.getText();
        }
        pst.setString(4, sem);
        
        String L_H =Lecture_Hours.getValue().toString();
        pst.setString(5, L_H);
        String T_H =Tutorial_Hours.getValue().toString();
        pst.setString(6, T_H);
        String P_H =Practical_Hours.getValue().toString();
        pst.setString(7, P_H);
        String E_H =Evaluation_Hours.getValue().toString();
        pst.setString(8, E_H);
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Added Successfully");
        
        Subject_Name.setText(" ");
        Subject_Code.setText(" ");
        year.setSelectedIndex(0);
        buttonGroup.clearSelection();
        Lecture_Hours.setValue(0);
        Tutorial_Hours.setValue(0);
        Practical_Hours.setValue(0);
        Evaluation_Hours.setValue(0);
        
        Error_SN.setText("");
        Error_SC.setText("");
        Error_OY.setText("");
        Error_OS.setText("");
        } catch (SQLException e) {
         Logger.getLogger(Add_Subject.class.getName()).log(Level.SEVERE, null, e);
        }   
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

